package com.addBankDetail;

public class BankGetSet {
	private String inputFName;
	private String inputLName;
	private String inputCardNumber;
	private String date;
	private String CVV;
	private String HouseName;
	private String StreetAddress;
	private String City;
	
	public String getInputFName() {
		return inputFName;
	}
	public void setInputFName(String inputFName) {
		this.inputFName = inputFName;
	}
	public String getInputLName() {
		return inputLName;
	}
	public void setInputLName(String inputLName) {
		this.inputLName = inputLName;
	}
	public String getInputCardNumber() {
		return inputCardNumber;
	}
	public void setInputCardNumber(String inputCardNumber) {
		this.inputCardNumber = inputCardNumber;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getCVV() {
		return CVV;
	}
	public void setCVV(String cVV) {
		CVV = cVV;
	}
	public String getHouseName() {
		return HouseName;
	}
	public void setHouseName(String houseName) {
		HouseName = houseName;
	}
	public String getStreetAddress() {
		return StreetAddress;
	}
	public void setStreetAddress(String streetAddress) {
		StreetAddress = streetAddress;
	}
	public String getCity() {
		return City;
	}
	public void setCity(String city) {
		City = city;
	}
}
